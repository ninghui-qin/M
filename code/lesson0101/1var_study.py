# 变量就是用来描述生活中的一些事物，在代码使用变量进行存储

# 1.比如沙陌老师的姓名
# name表示变量名称
# =表示赋值，意思是将后面的内容给前面的变量
# =号后面就是值，就是你要表示的内容
name = '沙陌'
# 可以试试将name进行输出，将他显式在控制台上
print(name) #print表示打印某个东东到控制台上，一般用来帮我们去调试代码
print('name') #注意这里打印的'name'是个值，不是变量，值是什么就打印什么
# 给变量重新赋值
name = '北河'
print(name)

age = 18
print(age)